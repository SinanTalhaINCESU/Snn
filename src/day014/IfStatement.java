package day014;

public class IfStatement {
    public static void main(String[] args) {

        /*
            x ve ye isminde iki adet tamsayı değişkeni tanımlayınız.
            x'i 0 ile ve y değişkenini 20 ile initialize ediniz.

            Eğer y'nin değeri 20'ye eşitse x'i 5 yapınız.
         */

        int x=0, y=20;

        if (y==20){
            x=5;
        }
        System.out.println(x);
    }
}
