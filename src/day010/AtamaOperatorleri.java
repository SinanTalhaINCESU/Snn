package day010;

public class AtamaOperatorleri {
    public static void main(String[] args) {

        int a=10;
        a=a+2;

        int b=10;
        b=b-2;

        int c=10;
        c=c*2;

        int d=10;
        d=d/2;

        int e=10;
        e=e%2;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
